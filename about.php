    <p class="card-text">The Bangladesh Road Transport Authority (BRTA) was constituted on 20 December 1987 vide SRO No. 303/Act/87/MVRT/1E-7/84(Part) under Section 2A of the Motor Vehicles Ordinance 1983 (Amended 1987) and on January 1988 Starts operations.
 
 BRTA was formed to ensure sound road transport management, discipline and road safety in the road transport sector. BRTA was formed under the then Ministry of Communications (now Ministry of Road Transport and Bridges) to implement the Motor Vehicles Ordinance 1983. BRTA is working under the Bangladesh Road Transport Act, 2017 to implement the Road Transport Act, 2018.
 
 The Chairman holds all the powers of BRTA. It is also the duty of the Chairman to review the proceedings issued by the Government from time to time.
 
 As per Revised Organogram BRTA currently has 8 Divisional Offices and 64 Circle Offices. Out of this 64 district circles and 6 metro circles.
 
 Divisional office is headed by Director(Eng:), Metro circle is headed by Deputy Director (Engr.). and circle office is headed by Assistant Director(Eng:).</p>
